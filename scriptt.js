function toggleSection(sectionId) {
    var section = document.getElementById(sectionId);
    section.classList.toggle('show');
}
document.getElementById('traducir-btn').addEventListener('click', function() {
    const objetivoFrances = document.getElementById('Objectif-frances');
    const objetivoEspanol = document.getElementById('Objetivo-espanol');

    if (objetivoFrances.style.display === 'block') {
        objetivoFrances.style.display = 'none';
        objetivoEspanol.style.display = 'block';
    } else {
        objetivoFrances.style.display = 'block';
        objetivoEspanol.style.display = 'none';
    }
});
